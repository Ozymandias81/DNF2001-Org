class WinVersion
{
public:
	U16 major;
	U16 minor;
public:
	WinVersion(void) : major(0),minor(0) {}
	void set(U32 Major,U32 Minor) {major=(U16)Major;minor=(U16)Minor;}
	U32 equal_or_better(U16 Major,U16 Minor)
	{
		if (major<Major)
			return FALSE;
		if (major>Major)
			return TRUE;
		if (minor<Minor)
			return FALSE;
		return TRUE;
	}
};
