// ---------------------------------------------------------------------------
// * CreateMRGmodel
// ---------------------------------------------------------------------------
// create a model from big lists above

MrgModel*
CreateMRGmodel(void)
{
	// create from lowest level up

	// create face set, set faces
	MrgFaceSet *fs = new MrgFaceSet;
	fs->setTriangleFaces((MrgUint16*)kFaces, ARRAYSIZE(kFaces)/3);
	
	// create point list from array
	MrgUint16 numPoints = ARRAYSIZE(kPoints) / 3;
	MrgCoord3D *points = new MrgCoord3D[numPoints];
	memcpy(points,kPoints,numPoints * sizeof(MrgCoord3D));

	// create minimum points list from other array
	MrgUint16 numMinPoints = ARRAYSIZE(kMinPoints) / 3;
	MrgCoord3D *minPoints = new MrgCoord3D[numMinPoints];
	memcpy(minPoints,kMinPoints,numMinPoints * sizeof(MrgCoord3D));

	// create vertex group and add geometry, minimum geometry
	MrgVertexData *vdata = new MrgVertexData;
	vdata->setGeometry(points, numPoints);
	vdata->setMinGeometry(minPoints, numMinPoints);
	// done with points & min points
	delete [] points;
	delete [] minPoints;
	

	// create hierarhcy
	MrgHier *hier = new MrgHier(*fs,*vdata);

	// create model, add hierarhcy
	MrgModel *model = new MrgModel;
	model->addMesh(hier);

	// init the MRG
	model->initMRG();

	// force normal calculation
	model->calcVertexNorms();

	return model;
}

// ---------------------------------------------------------------------------
// * LoadMRGmodel
// ---------------------------------------------------------------------------
// Attempt to load a persisted MRG model for example (example.mrg)
// if unable to load, create a new one and then attempt to save that

MrgModel*
LoadMRGmodel(const char* filename)
{
	MrgModel* rtrn = NULL;

	if (filename == NULL)
	{
		// try to load "example.mrg" from disk
		filename = "example.mrg";
	}

	// get model from file
	rtrn = MrgModel::createFromFile(filename);
	if (rtrn)
	{
		// calculate normals!
		rtrn->calcVertexNorms();
	}
	else
	{
		// couldn't read from stream, create model
		rtrn = CreateMRGmodel();

		if (rtrn)
		{
			// try to save it:
			rtrn->writeFile(filename);
		}
	}

	return rtrn;
}
