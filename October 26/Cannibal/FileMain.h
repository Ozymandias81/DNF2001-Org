class IFileBase
{
public:
	virtual NBool Close()=0;
	virtual NBool SeekStart(NDword inPos)=0;
	virtual NBool SeekCurrent(NSDword inPos)=0;
	virtual NBool SeekEnd(NDword inPos)=0;
	virtual NDword Tell()=0;
	virtual NDword Size()=0;
};

